    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int id_dep_new=0;
        int id_dep_old=0;
        if (!nom_mun_actual.getText().equals("")) {
            if (!nuevo_nom_mun.getText().equals("")) {
                Conexion con = new Conexion();
                con.conexion();
                ResultSet r;
                try {
                    r = con.s.executeQuery ("SELECT * FROM t_departamentos WHERE NOMBRE_DEP='"+cb_dep.getSelectedItem().toString().toUpperCase()+"' ORDER BY NOMBRE_DEP ASC;");
                    if (r.next()) {
                        id_dep_new = r.getInt("ID_DEP");
                    }
                    r = con.s.executeQuery ("SELECT * FROM `t_municipios` WHERE NOMBRE_MUN = '"+nuevo_nom_mun.getText().toUpperCase()+"' AND ID_DEP="+id_dep_new);
                    if(r.next()){
                        JOptionPane.showMessageDialog(this,"El MUNICIPIO que intenta ingresar ya existe","Error",JOptionPane.ERROR_MESSAGE);
                    }
                    else{
                        int conf = JOptionPane.showConfirmDialog(this,"Esta seguro que desea continuar?","Confirmación",JOptionPane.OK_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE);
                        if (conf == JOptionPane.YES_OPTION) {
                            r = con.s.executeQuery ("SELECT * FROM t_departamentos WHERE NOMBRE_DEP='"+nom_dep_actual.getText().toUpperCase()+"';");
                            if (r.next()) {
                                id_dep_old = r.getInt("ID_DEP");
                            }
                            con.s.executeUpdate("UPDATE `t_municipios` SET `NOMBRE_MUN`='"+nuevo_nom_mun.getText().toUpperCase()+"',`ID_DEP`="+id_dep_new+"  WHERE `NOMBRE_MUN`='"+nom_mun_actual.getText().toUpperCase()+"' AND ID_DEP="+id_dep_old);
                            JOptionPane.showMessageDialog(this,"El MUNICIPIO fue modificado correctamente","Información",JOptionPane.INFORMATION_MESSAGE);
                            con.cerrar();
                            this.dispose();
                        }

                    }
                    con.cerrar();
                } catch (Exception e) {
                    con.cerrar();
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(this,e,"Error",JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this,"Digite el nuevo nombre de la MUNICIPIO que desea modificar","Error",JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this,"Digite el nombre de la MUNICIPIO que desea modificar","Error",JOptionPane.ERROR_MESSAGE);
        }
           
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

