    private void busc_munKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_busc_munKeyTyped
        // TODO add your handling code here:
        listModel.removeAllElements();
        Conexion con = new Conexion();
        con.conexion();
        ResultSet r;
        try{
            r = con.s.executeQuery ("SELECT *\n" +
                                    "FROM\n" +
                                    "    t_municipios\n" +
                                    "    INNER JOIN t_departamentos \n" +
                                    "        ON (t_municipios.ID_DEP = t_departamentos.ID_DEP) WHERE NOMBRE_MUN LIKE '%"+busc_mun.getText()+"%' ORDER BY NOMBRE_MUN ASC;");
            while(r.next()){
                String str=r.getString("NOMBRE_MUN");
                String str1=r.getString("NOMBRE_DEP");
                listModel.addElement(str+"-"+str1);
            }
            con.cerrar();
        }catch(SQLException j){
            con.cerrar();
            JOptionPane.showMessageDialog(null,j,"Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_busc_munKeyTyped

    private void list_munMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_list_munMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 1) {
            String str_mun="";
            String str_dep="";
            StringTokenizer tokens=new StringTokenizer(list_mun.getSelectedValue(), "-");
            while(tokens.hasMoreTokens()){
                str_mun=tokens.nextToken();
                str_dep=tokens.nextToken();
//                System.out.println(tokens.nextToken());
            }
            Conexion con = new Conexion();
            con.conexion();
            ResultSet r;
            try{
                r = con.s.executeQuery ("SELECT *\n" +
                                        "FROM\n" +
                                        "    t_municipios\n" +
                                        "    INNER JOIN t_departamentos \n" +
                                        "        ON (t_municipios.ID_DEP = t_departamentos.ID_DEP) WHERE NOMBRE_MUN = '"+str_mun+"' AND NOMBRE_DEP = '"+str_dep+"';");
                if(r.next()){
                    String str=r.getString("NOMBRE_MUN");
                    nom_mun_actual.setText(str);
                    nuevo_nom_mun.setText(str);
                    String str1=r.getString("NOMBRE_DEP");
                    nom_dep_actual.setText(str1);
                    cb_dep.setSelectedItem(str1);
                }
                con.cerrar();
            }catch(SQLException j){
                con.cerrar();
                JOptionPane.showMessageDialog(null,j,"Error",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_list_munMouseClicked

