private void load_cert_alt(String c, JTable t){
    clear_table(t);
    DefaultTableModel modelo = (DefaultTableModel)t.getModel();
    Object [] fila = new Object[2];
    Conexion con = new Conexion();
    con.conexion();
    ResultSet r;
    try{
        r = con.s.executeQuery ("SELECT *\n" +
                                "FROM\n" +
                                "`t_cert_alturas`\n" +
                                "WHERE ID_EMP = "+c+" ORDER BY FECHA_CERT DESC;");
        while(r.next()){
            modelo.addRow(fila);
            modelo.setValueAt(new SimpleDateFormat("dd-MM-yyyy").format(new SimpleDateFormat("yyyy-MM-dd").parse(r.getString("FECHA_EXAM_CERT"))),t.getRowCount()-1,0);
            modelo.setValueAt(new SimpleDateFormat("dd-MM-yyyy").format(new SimpleDateFormat("yyyy-MM-dd").parse(r.getString("FECHA_CERT"))),t.getRowCount()-1,1);
        }
        con.cerrar();
    }catch(ParseException | SQLException j){
        con.cerrar();
        JOptionPane.showMessageDialog(null,j,"Error",JOptionPane.ERROR_MESSAGE);
    }   
}
