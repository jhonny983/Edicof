private void load_riesgo(String c, JTable t){
    clear_table(t);
    DefaultTableModel modelo = (DefaultTableModel)t.getModel();
    Object [] fila = new Object[2];
    Conexion con = new Conexion();
    con.conexion();
    ResultSet r;
    try{
        r = con.s.executeQuery ("SELECT *\n" +
                                "FROM\n" +
                                "    `t_riesgo`\n" +
                                "    INNER JOIN `t_tip_riesgo` \n" +
                                "        ON (`t_riesgo`.`ID_TIPO` = `t_tip_riesgo`.`ID_TIPO`)\n" +
                                "WHERE ID_EMP = "+c+" ORDER BY FECHA_ENC DESC;");
        while(r.next()){
            modelo.addRow(fila);
            modelo.setValueAt(new SimpleDateFormat("dd-MM-yyyy").format(new SimpleDateFormat("yyyy-MM-dd").parse(r.getString("FECHA_ENC"))),t.getRowCount()-1,0);
            modelo.setValueAt(r.getString("NOMBRE_TIPO"),t.getRowCount()-1,1);
        }
        con.cerrar();
    }catch(ParseException | SQLException j){
        con.cerrar();
        JOptionPane.showMessageDialog(null,j,"Error",JOptionPane.ERROR_MESSAGE);
    }
}
private void clear_table(JTable t){
    DefaultTableModel modelo = (DefaultTableModel)t.getModel();
    while(t.getRowCount()!=0){
        modelo.removeRow(t.getRowCount()-1);
    }
    t.setModel(modelo);
}
private void load_emp(String c){
    Conexion con = new Conexion();
    con.conexion();
    ResultSet r;
    try{
        r = con.s.executeQuery ("SELECT *\n" +
                                "FROM\n" +
                                "`t_empleados`\n" +
                                "WHERE ID_EMP = "+c);
        if(r.next()){
            String nombre1 = r.getString("NOMBRE_1_EMP");
            String nombre2 = r.getString("NOMBRE_2_EMP");
            String apellido1 = r.getString("APELLIDO_1_EMP");
            String apellido2 = r.getString("APELLIDO_2_EMP");
            nombre1=nombre1+" ";
            apellido1=apellido1+" ";
            if (!nombre2.equals("")) {
                nombre2+=" ";
            }
            if (!apellido2.equals("")) {
                apellido2+=" ";
            }
            cedula_emp.setText(r.getString("ID_EMP"));
            nombre_emp.setText(nombre1+nombre2+apellido1+apellido2);
        }
        con.cerrar();
    }catch(SQLException j){
        con.cerrar();
        JOptionPane.showMessageDialog(null,j,"Error",JOptionPane.ERROR_MESSAGE);
    } 
}
