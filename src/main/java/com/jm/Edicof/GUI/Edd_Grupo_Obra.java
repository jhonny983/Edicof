public void load(int id){
    Conexion con = new Conexion();
    con.conexion();
    ResultSet r;
    try {
        r = con.s.executeQuery ("SELECT * FROM T_GRUPO_EMPRESA WHERE ID_GRUPO ="+id);
        if (r.next()) {
            nombre_grupo.setText(r.getString("NOMBRE_GRUPO"));
            desc_grupo.setText(r.getString("DESC_GRUPO"));
        }
        
    } catch (SQLException | HeadlessException e) {
        con.cerrar();
        e.printStackTrace();
        JOptionPane.showMessageDialog(this,e,"Error",JOptionPane.ERROR_MESSAGE);
    }
}
