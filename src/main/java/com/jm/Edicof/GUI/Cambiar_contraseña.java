    public void cargar(){
        String cont_act = null;
        String cargo = null;
        String login;
        String s_ap = null;
        String p_ap = null;
        String s_nom = null;
        String p_nom = null;
        Conexion con = new Conexion();
        con.conexion();
        ResultSet r;
        try{
            r = con.s.executeQuery ("SELECT * FROM t_usuarios u, t_roles r where ID_USUARIO="+Main.id_usuario+" and u.ID_rol =r.ID_rol");
            if(r.next()){
                if (r.getString("NOMBRE_USUARIO")!=null) {
                    p_nom = r.getString("NOMBRE_USUARIO").toUpperCase();
                }else{
                    p_nom = "";
                }
                if (r.getString("NOMBRE_2_USUARIO")!=null) {
                    s_nom = r.getString("NOMBRE_2_USUARIO").toUpperCase();
                }else{
                    s_nom = "";
                }
                if (r.getString("APELLIDO_USUARIO")!=null) {
                    p_ap = r.getString("APELLIDO_USUARIO").toUpperCase();
                }else{
                    p_ap = "";
                }
                if (r.getString("APELLIDO_2_USUARIO")!=null) {
                    s_ap = r.getString("APELLIDO_2_USUARIO").toUpperCase();
                }else{
                    s_ap = "";
                }
                Nombres_Emp.setText(p_nom+" "+s_nom);
                Apellidos_emp.setText(p_ap+" "+s_ap);
                Rol_emp.setText(r.getString("NOMBRE_ROL"));
                //con.cerrar();
            }
            con.cerrar();
        }catch(Exception j){
            con.cerrar();
            j.printStackTrace();
            JOptionPane.showMessageDialog(null,j,"Error!",JOptionPane.ERROR_MESSAGE);
        }
    }
