private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String cont_nueva;
    String ver_cont;
    String p = null;
    if(!Cont_actual.getPassword().equals("")){
        Conexion con = new Conexion();
        con.conexion();
        ResultSet r;
        try{
            r = con.s.executeQuery ("SELECT * FROM t_usuarios WHERE ID_USUARIO="+Main.id_usuario);
            if(r.next()){
                //p=Encriptar.Desencriptar(r.getString("CONT_USUARIO"));
                p=r.getString("CONT_USUARIO");
            }
            con.cerrar();
        }catch(Exception j){
            con.cerrar();
            JOptionPane.showMessageDialog(null,j, "Error",JOptionPane.ERROR_MESSAGE);
        }
        //if(p.equals(new String(Cont_actual.getPassword()))){
        if(p.equals(Encriptar.getMD5(new String(Cont_actual.getPassword())))){
            if(!new String(Con_nueva.getPassword()).equals("") && !new String(Ver_cont.getPassword()).equals("")){
                    cont_nueva = new String(Con_nueva.getPassword());
                    ver_cont = new String(Ver_cont.getPassword());
                    if(ver_cont.equals(cont_nueva)){
                        con.conexion();
                        try{
                            //con.s.executeUpdate("UPDATE T_USUARIOS SET CONT_USUARIO='"+Encriptar.Encriptar(ver_cont)+"' WHERE ID_USUARIO="+Main.id_usuario);
                            con.s.executeUpdate("UPDATE T_USUARIOS SET CONT_USUARIO='"+Encriptar.getMD5(ver_cont)+"' WHERE ID_USUARIO="+Main.id_usuario);
                            con.cerrar();
                        }catch(Exception j){
                            con.cerrar();
                            JOptionPane.showMessageDialog(null,j, "Error",JOptionPane.ERROR_MESSAGE);
                        }
                        JOptionPane.showMessageDialog(null,"La contraseña ha sido cambiada","Confirmacion",JOptionPane.INFORMATION_MESSAGE);
                        Nombres_Emp.setText("");
                        Apellidos_emp.setText("");
                        Rol_emp.setText("");
                        Cont_actual.setText("");
                        Con_nueva.setText("");
                        Ver_cont.setText("");
                        this.dispose();
                    
                    }else{
                        JOptionPane.showMessageDialog(null,"Las contrasenas no coinciden","Error!",JOptionPane.ERROR_MESSAGE);
                    }

            }else{
                JOptionPane.showMessageDialog(null,"Por Favor complete todos los campos","Error!",JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null,"La clave actual es incorrecta","Advertencia",JOptionPane.WARNING_MESSAGE);
        }
    }else{
        JOptionPane.showMessageDialog(null,"Por Favor ingrese la contraseña actual","Error!",JOptionPane.ERROR_MESSAGE);
    }
            
}//GEN-LAST:event_jButton1ActionPerformed

