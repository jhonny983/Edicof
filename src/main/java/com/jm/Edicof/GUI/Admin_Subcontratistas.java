private void load_sub(String n){
    Conexion con = new Conexion();
    con.conexion();
    ResultSet r;
    try{
        r = con.s.executeQuery ("SELECT *\n" +
                                "FROM\n" +
                                "    `t_rel_subcontratista`\n" +
                                "    INNER JOIN `t_obra` \n" +
                                "        ON (`t_rel_subcontratista`.`ID_OBRA` = `t_obra`.`ID_OBRA`)\n" +
                                "    INNER JOIN `t_empresas` \n" +
                                "        ON (`t_rel_subcontratista`.`ID_CONTR` = `t_empresas`.`ID_EMPRESA`)\n" +
                                "WHERE ID_SUBCONTR='"+n+"';");
        while(r.next()){
            modelo.addRow(fila);
            subcontratistas.setModel(modelo);
            modelo.setValueAt(r.getString("ID_EMPRESA"), subcontratistas.getRowCount()-1, col_nit_contr);
            modelo.setValueAt(r.getString("NOMBRE_EMPRESA"), subcontratistas.getRowCount()-1, col_contr);
            modelo.setValueAt(r.getString("NOMBRE_OBRA"), subcontratistas.getRowCount() - 1, col_obra);
            modelo.setValueAt(r.getString("F_INICIO"), subcontratistas.getRowCount() - 1, col_f_ini);
            modelo.setValueAt(r.getString("F_FINAL"), subcontratistas.getRowCount() - 1, col_f_fin);
            modelo.setValueAt(r.getString("CONTRATO"), subcontratistas.getRowCount() - 1, col_contrato);
            modelo.setValueAt(r.getString("FUNCION"), subcontratistas.getRowCount() - 1, col_actividad);
            
        }
        con.cerrar();
    }catch(SQLException j){
        con.cerrar();
        j.printStackTrace();
    }
}
private void clear_sub(){
    modelo = (DefaultTableModel)subcontratistas.getModel(); 
    while(subcontratistas.getRowCount()!=0){
        modelo.removeRow(subcontratistas.getRowCount()-1);
    }
    subcontratistas.setModel(modelo);
}
