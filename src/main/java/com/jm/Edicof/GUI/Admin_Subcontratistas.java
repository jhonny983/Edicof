private String load_empresa(String nit){
    String ret="";
    Conexion con = new Conexion();
    con.conexion();
    ResultSet r;
    try{
        r = con.s.executeQuery ("SELECT *\n" +
                                "FROM\n" +
                                "`t_empresas`\n" +
                                "WHERE ID_EMPRESA = "+nit);
        if(r.next()){
            ret = r.getString("NOMBRE_EMPRESA");
        }
        con.cerrar();
    }catch(SQLException j){
        con.cerrar();
        JOptionPane.showMessageDialog(null,j,"Error",JOptionPane.ERROR_MESSAGE);
    } 
    return ret;
}
