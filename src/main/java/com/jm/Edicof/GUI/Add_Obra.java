    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id_dep="";
        String id_mun="";
        if (!nombre_obra.getText().equals("")) {
            if (cb_mun.getItemCount()!=0) {
                if (!cb_mun.getSelectedItem().equals("")) {
                    Conexion con = new Conexion();
                    con.conexion();
                    ResultSet r;
                    try {
                        r = con.s.executeQuery ("SELECT *\n" +
                                                "FROM\n" +
                                                "    t_obra\n" +
                                                "    INNER JOIN t_municipios \n" +
                                                "        ON (t_obra.ID_MUN_OBRA = t_municipios.ID_MUN) WHERE ID_OBRA = "+centro_costo.getText().toUpperCase());
                        if(r.next()){
                            JOptionPane.showMessageDialog(this,"La OBRA que intenta ingresar ya existe","Error",JOptionPane.ERROR_MESSAGE);
                        }
                        else{
                            int conf = JOptionPane.showConfirmDialog(this,"Esta seguro que desea continuar?","Confirmación",JOptionPane.OK_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE);
                            if (conf == JOptionPane.YES_OPTION) {
                                r = con.s.executeQuery ("SELECT * FROM T_DEPARTAMENTOS WHERE NOMBRE_DEP='"+cb_dep.getSelectedItem().toString().toUpperCase()+"';");
                                if (r.next()) {
                                    id_dep = r.getString("ID_DEP");
                                }
                                r = con.s.executeQuery ("SELECT * FROM T_MUNICIPIOS WHERE NOMBRE_MUN='"+cb_mun.getSelectedItem().toString().toUpperCase()+"' AND ID_DEP ="+id_dep);
                                if (r.next()) {
                                    id_mun = r.getString("ID_MUN");
                                    con.s.executeUpdate("INSERT INTO `t_obra`(`ID_OBRA`,`NOMBRE_OBRA`,`ID_MUN_OBRA`,`ID_GRUPO`) VALUES ("+centro_costo.getText().trim()+",'"+nombre_obra.getText().toUpperCase().trim()+"',"+id_mun+",(SELECT ID_GRUPO FROM T_GRUPO_EMPRESA WHERE NOMBRE_GRUPO='"+grupos.getSelectedItem()+"'))");
                                    JOptionPane.showMessageDialog(this,"La OBRA fue ingresada correctamente","Información",JOptionPane.INFORMATION_MESSAGE);
                                    con.cerrar();
                                    this.dispose();
                                }else{
                                    JOptionPane.showMessageDialog(this,"El municipio de la OBRA que intenta ingresar no existe","Error",JOptionPane.ERROR_MESSAGE);
                                }
                            }
                        }
                        con.cerrar();
                    } catch (SQLException | HeadlessException e) {
                        con.cerrar();
                        e.printStackTrace();
                        JOptionPane.showMessageDialog(this,e,"Error",JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(this,"Digite el nombre del municipio de la OBRA","Error",JOptionPane.ERROR_MESSAGE);
                }
            }
            
            
        } else {
            JOptionPane.showMessageDialog(this,"Digite el nombre de la OBRA","Error",JOptionPane.ERROR_MESSAGE);
        }
           
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

