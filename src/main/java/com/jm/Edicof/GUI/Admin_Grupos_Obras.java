public void clear_grupos(){
    modelo = (DefaultTableModel)jTable1.getModel(); 
    while(jTable1.getRowCount()!=0){
        modelo.removeRow(jTable1.getRowCount()-1);
    }
}
    
    
public void load(){
    Conexion con = new Conexion();
    con.conexion();
    ResultSet r;
    try {
        r = con.s.executeQuery ("SELECT * FROM T_GRUPO_EMPRESA");
        while (r.next()) {
            modelo.addRow(fila);
            modelo.setValueAt(r.getString("ID_GRUPO"),jTable1.getRowCount()-1,0);
            modelo.setValueAt(r.getString("NOMBRE_GRUPO"),jTable1.getRowCount()-1,1);
            modelo.setValueAt(r.getString("DESC_GRUPO"),jTable1.getRowCount()-1,2);
        }
        
    } catch (SQLException | HeadlessException e) {
        con.cerrar();
        e.printStackTrace();
        JOptionPane.showMessageDialog(this,e,"Error",JOptionPane.ERROR_MESSAGE);
    }
}



